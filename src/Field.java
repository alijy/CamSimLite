public class Field {
	
	Double minX;
	Double minY;
	Double maxX;
	Double maxY;
	
	/*
	 * Constructor
	 */
	public Field (Double minX, Double minY, Double maxX, Double maxY) {
		
		this.minX = minX;
		this.minY = minY;
		this.maxX = maxX;
		this.maxY = maxY;
	}

}
